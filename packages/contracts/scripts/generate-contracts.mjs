import { existsSync, mkdirSync, readFileSync, readdirSync, writeFileSync } from "node:fs";
import { dirname, join, relative } from "node:path";
import { fileURLToPath } from "node:url";

const packageRoot = dirname(dirname(fileURLToPath(import.meta.url)));
const schemaRoot = join(packageRoot, "schemas");
const generatedRoot = join(packageRoot, "generated");
const generatorVersion = "2026-06-09.contracts.1";
const mode = process.argv.includes("--check") ? "check" : "write";

function readJson(path) {
  return JSON.parse(readFileSync(path, "utf8"));
}

function stableStringify(value, space = 2) {
  return `${JSON.stringify(sortObject(value), null, space)}\n`;
}

function sortObject(value) {
  if (Array.isArray(value)) return value.map(sortObject);
  if (value === null || typeof value !== "object") return value;
  return Object.fromEntries(Object.keys(value).sort().map((key) => [key, sortObject(value[key])]));
}

function toPascalCase(value) {
  return value
    .replace(/\.schema\.json$/, "")
    .split(/[-_]/)
    .map((part) => `${part.charAt(0).toUpperCase()}${part.slice(1)}`)
    .join("");
}

function normalizeForCheck(path, text) {
  if (!existsSync(path)) return text;
  const current = readFileSync(path, "utf8");
  if (path.endsWith(".ts")) {
    return text.replace(/generatedAt: ".*?"/, current.match(/generatedAt: ".*?"/)?.[0] ?? 'generatedAt: ""');
  }

  const next = JSON.parse(text);
  const existing = JSON.parse(current);
  if (next.generated) {
    next.generated.generatedAt = existing.generated?.generatedAt ?? next.generated.generatedAt;
  }
  if (next.info?.["x-jami-generated"]) {
    next.info["x-jami-generated"].generatedAt = existing.info?.["x-jami-generated"]?.generatedAt ?? next.info["x-jami-generated"].generatedAt;
  }
  return stableStringify(next);
}

function writeOrCheck(relativePath, content, failures) {
  const path = join(packageRoot, relativePath);
  if (mode === "write") {
    mkdirSync(dirname(path), { recursive: true });
    writeFileSync(path, content);
    return;
  }

  if (!existsSync(path)) {
    failures.push(`${relativePath} is missing; run pnpm --filter @jami-studio/harness-contracts generate`);
    return;
  }

  const expected = normalizeForCheck(path, content);
  const actual = normalizeForCheck(path, readFileSync(path, "utf8"));
  if (actual !== expected) {
    failures.push(`${relativePath} is out of date; run pnpm --filter @jami-studio/harness-contracts generate`);
  }
}

const schemaFiles = readdirSync(schemaRoot)
  .filter((file) => file.endsWith(".schema.json"))
  .sort();

const schemas = schemaFiles.map((file) => ({
  file,
  exportName: `${toPascalCase(file)}Schema`,
  typeName: toPascalCase(file),
  schema: readJson(join(schemaRoot, file)),
}));

const generatedAt = new Date().toISOString();
const schemaExports = schemas
  .map(({ exportName, schema }) => `export const ${exportName} = ${JSON.stringify(sortObject(schema), null, 2)} as const;`)
  .join("\n\n");
const typeExports = schemas
  .map(({ typeName }) => `export type ${typeName} = Record<string, unknown>;`)
  .join("\n");
const registryEntries = schemas
  .map(({ exportName, file, schema, typeName }) => `  ${JSON.stringify(schema.title)}: {
    file: ${JSON.stringify(`schemas/${file}`)},
    id: ${JSON.stringify(schema.$id)},
    schema: ${exportName},
    typeName: ${JSON.stringify(typeName)}
  }`)
  .join(",\n");

const contractsTs = `// Generated by packages/contracts/scripts/generate-contracts.mjs.
// Do not edit by hand; run pnpm --filter @jami-studio/harness-contracts generate.

export const generatedContractMetadata = {
  generatedAt: "${generatedAt}",
  generatorVersion: "${generatorVersion}",
  source: "packages/contracts/schemas/*.schema.json"
} as const;

${schemaExports}

export const harnessContractSchemas = {
${registryEntries}
} as const;

export type HarnessContractName = keyof typeof harnessContractSchemas;
${typeExports}
`;

const reference = {
  generated: {
    generatedAt,
    generatorVersion,
    source: "packages/contracts/schemas/*.schema.json",
  },
  package: "@jami-studio/harness-contracts",
  schemas: schemas.map(({ file, schema }) => ({
    title: schema.title,
    id: schema.$id,
    file: `schemas/${file}`,
    required: schema.required ?? [],
  })),
  studioUiHandshake: {
    consume: [
      "generated/contracts.ts for schema ids and anchor names",
      "generated/openapi.json for component schema import",
      "fixtures/** for positive and negative compatibility cases",
    ],
    ownedByHarness: [
      "policy-gated actionRef",
      "artifactView provenance",
      "runEvent evidence and renderer states",
      "evidencePacket provenance and redaction",
      "threatModelFixtureCatalog risk-to-fixture mapping",
    ],
    ownedByStudioUi: [
      "resident component implementation",
      "token output",
      "registry packaging",
      "renderer execution behavior",
      "suite install behavior",
    ],
  },
};

const openapi = {
  openapi: "3.1.0",
  info: {
    title: "Jami Harness Contracts",
    version: "2026-06-09",
    "x-jami-generated": {
      generatedAt,
      generatorVersion,
      source: "packages/contracts/schemas/*.schema.json",
    },
  },
  paths: {},
  components: {
    schemas: Object.fromEntries(schemas.map(({ schema, typeName }) => [typeName, schema])),
  },
};

const failures = [];
writeOrCheck("generated/contracts.ts", contractsTs, failures);
writeOrCheck("generated/reference.json", stableStringify(reference), failures);
writeOrCheck("generated/openapi.json", stableStringify(openapi), failures);

if (failures.length > 0) {
  console.error("contracts generation drift check failed");
  for (const failure of failures) {
    console.error(`- ${failure}`);
  }
  process.exit(1);
}

console.log(mode === "check" ? "contracts generation drift check passed" : `generated ${relative(packageRoot, generatedRoot)} artifacts`);
